    public void cargarTablaJugadores(List<Jugador> jugadores) {
        try {
            DefaultTableModel modelo = new DefaultTableModel();

            modelo.addColumn("Dorsal");
            modelo.addColumn("Nombre");
            modelo.addColumn("Puntos");
            modelo.addColumn("Rebotes");
            modelo.addColumn("Asistencias");

            Object[] registroLeido = new Object[5];

            for (Jugador jugador : jugadores) {
                registroLeido[0] = jugador.getDorsal();
                registroLeido[1] = jugador.getNombre();
                registroLeido[2] = jugador.getPuntos();
                registroLeido[3] = jugador.getRebotes();
                registroLeido[4] = jugador.getAsistencias();

                modelo.addRow(registroLeido);
            }

            tableJugadores.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void cargarListaConsultas() {
        DefaultListModel<String> listModel = new DefaultListModel<>();
            
        for (String consulta : consultasRealizadas) {
            listModel.addElement(consulta);
        }
            
        lstConsultas.setModel(listModel);
    }
    
    public List<Jugador> contarAnotadores(int puntos) {
        List<Jugador> jugadores = daoJugador.getJugadores();
        List<Jugador> jugadoresFiltrados = new ArrayList<>();
        
        jugadores.stream().filter(jugador -> jugador.getPuntos() > puntos).forEach((jugador -> jugadoresFiltrados.add(jugador)));
        
        return jugadoresFiltrados;
    }


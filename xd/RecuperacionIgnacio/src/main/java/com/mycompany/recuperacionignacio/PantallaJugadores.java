    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            String consulta = txtConsulta.getText();
        
            if (consulta.isEmpty()) {
                throw new ErrorCuadroVacio();
            } else {
                List<Jugador> jugadores = new ArrayList<>();
                
                if (consulta.contains("puntos ")) {
                    int puntos = Integer.parseInt(consulta.substring(7));
                    jugadores = contarAnotadores(puntos);
                } else {
                    jugadores = daoJugador.realizarConsulta(consulta);
                }
                
                if (jugadores.isEmpty()) {
                    throw new ErrorNoDatos();
                } else {
                    if (!consultasRealizadas.contains(consulta)) {
                        consultasRealizadas.add(consulta);
                    }
                    cargarListaConsultas();
                    cargarTablaJugadores(jugadores);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


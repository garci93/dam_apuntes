/*Un inmueble viene dado por su ubicación y los metros
cuadrados que ocupa. Hay dos tipos de inmuebles: superficies y
construcciones. Los primeros tienen su precio por metro cuadrado. A
su vez, hay dos tipos de superficies: solares y plazas de garaje. Los
solares pueden estar en zona rústica o urbana. Las plazas de garaje
pueden formar parte de un garaje público o de uno privado. Por su
parte, las construcciones pueden ser nuevas o de segunda mano. Hay
dos tipos de construcciones: viviendas y locales comerciales. Las
viviendas tienen precio, número de habitaciones y piso. Los locales
tienen precio por metro cuadrado.*/